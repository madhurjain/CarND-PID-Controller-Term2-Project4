#ifndef PID_H
#define PID_H

class PID {
public:
  /*
  * Errors
  */
  double p_error;
  double i_error;
  double d_error;

  /*
  * Coefficients
  */ 
  double Kp;
  double Ki;
  double Kd;

  /*
  * Added for Twiddle
  */
  enum State { FIRSTRUN, BASELINE, CORRECTION, DONE };
  State nextState;
  bool resetCar;
  int tuneParameter;
  double dp[3];
  double tolerance;
  unsigned int iteration;
  unsigned int stepCount;
  double newErr;
  double bestErr;
  bool moveToNextParamTuning;

  /*
  * Constructor
  */
  PID();

  /*
  * Destructor.
  */
  virtual ~PID();

  /*
  * Initialize PID.
  */
  void Init(double Kp, double Ki, double Kd);

  /*
  * Update the PID error variables given cross track error.
  */
  void UpdateError(double cte);

  /*
  * Calculate the total PID error.
  */
  double TotalError();
};

#endif /* PID_H */
